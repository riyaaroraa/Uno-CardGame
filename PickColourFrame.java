    private void redButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_redButtonActionPerformed
        wildColour = UnoCard.Colour.Red;
        JLabel message = new JLabel("The Wild Card Colour is Red!!");
        message.setFont(new Font("Arial", Font.BOLD,48));
        JOptionPane.showMessageDialog(null, message);
        allow = true;
        this.dispose();
        popUp.declaredColour = UnoCard.Colour.Red;
        popUp.gameStage.setPidName(popUp.game.getCurrentPlayer());
        popUp.gameStage.setButtonIcons();
        /*popUp.topCardButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/PNGs/small/" + popUp.game.getTopCardImage())));*/
        popUp.topCardButton.setIcon(new javax.swing.ImageIcon("C:\\Finder\\images\\PNGs\\small\\" + popUp.game.getTopCardImage()));
        popUp.game.setCardColour(UnoCard.Colour.Red);
        popUp.dispose();
    }//GEN-LAST:event_redButtonActionPerformed

