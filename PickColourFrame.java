    public UnoCard.Colour choseColour(UnoCard card) {
        if (card.getColour() == UnoCard.Colour.Wild){
            this.setVisible(true);
            this.setResizable(false);
            this.setBounds(100,150,600,700);
        }
        return card.getColour();
        
    }

